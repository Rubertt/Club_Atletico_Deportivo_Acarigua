        <h3 style="margin-top:0;">Nuestra misión</h3>
        <p>
            El <strong>Club Atlético Deportivo Acarigua</strong> nació en 2019 con el propósito
            de formar niños, adolescentes y jóvenes a través del deporte, promoviendo valores
            cristianos, disciplina y excelencia. Nuestra sede opera en el municipio Páez,
            estado Portuguesa, y hoy más de 250 personas son beneficiadas directa e
            indirectamente por nuestras actividades.
        </p>

        <h3>Nuestro enfoque</h3>
        <p>
            Creemos que el deporte es una herramienta poderosa para la formación integral.
            Por eso trabajamos el desarrollo físico, técnico y humano de cada atleta,
            acompañados de un equipo técnico comprometido y un sistema riguroso de
            seguimiento antropométrico y de rendimiento.
        </p>

        <h3>Categorías</h3>
        <p>
            Organizamos el trabajo por categorías de edad (Sub-8, Sub-10, Sub-12, Sub-14,
            Sub-16 y Sub-18), cada una con objetivos técnicos y pedagógicos adaptados
            a la etapa de desarrollo.
        </p>
